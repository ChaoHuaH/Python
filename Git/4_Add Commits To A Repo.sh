# Add files from the working directory to the staging index
git add <file1> <file2> <files>
git add . # add all

# Take files from the staging index and save them in the repository
git commit # would open the editor and need to type the commit msg
git commit -m "msg" # add msg directly w/o opening the editor

# Displays the difference between two versions of a file
# used to see changes that have been made but haven't been committed
git diff




