# Create brand new repositories(repos) on your computer
git init

# Copy existing repos from somewhre else to your local computer
# TIP: Now before you clone anything, 
# make sure you are located in the correct directory on the command line. 
# Cloning a project creates a new directory and places the cloned Git repository in it. 
# The problem is that you can't have nested Git repositories. 
# So make sure the terminal's current working directory isn't located in a Git repository. 
git clone "path-to-repository-to-clone"               # same project name
git clone "path-to-repository-to-clone" new_repo_name # change project name to new_repo_name

# Check the status of a repo
git status




